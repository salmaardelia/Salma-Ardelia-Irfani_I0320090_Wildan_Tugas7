#Soal nomor 2
import math
#Fungsi sqrt
x = 144
y = 256
z = 400
print('Nilai sqrt x = ', math.sqrt(x))
print('Nilai sqrt y = ', math.sqrt(y))
print('Nilai sqrt z = ', math.sqrt(z))

#Fungsi floor
p = 24.567
q = 56.123
print('Nilai floor p = ', math.floor(p))
print('Nilai floor q = ', math.floor(q))

#Fungsi ceil
a = 10.32
b = 87.12
print('Nilai ceil a = ', math.ceil(a))
print('Nilai ceil b = ', math.ceil(b))

#Fungsi fabs
P = -50.24
Q = -24.08
print('Nilai fabs P = ', math.fabs(P))
print('Nilai fabs Q = ', math.fabs(Q))